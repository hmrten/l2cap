packet_t *alloc_packet(const uchar *data, ushort size)
{
	packet_t *pkt;

	pkt = malloc(sizeof(packet_t)-1 + size);
	pkt->size = size;
	pkt->id = data[2];
	pkt->dsize = size-3;
	memcpy(pkt->data, data+3, pkt->dsize);

	return pkt;
}

void dbg_dump_packet(const packet_t *pkt)
{
	ushort i;

	log_msg("", "[%.2X] (%hu) Dump...\r\n", pkt->id, pkt->size);
	for (i=0; i<pkt->dsize; ++i) {
		log_msg("", "%.2X ", pkt->data[i]);
	}
	log_msg("", "\r\n");
}

